CREATE TABLE sms_history ( 
SMS_ID int NOT NULL AUTO_INCREMENT, 
USER_ID varchar(255) DEFAULT NULL, 
MEMBER_ID varchar(255) DEFAULT NULL, 
MESSAGE text, 
NUM_SEGMENTS int DEFAULT NULL, 
DIRECTION varchar(255) DEFAULT NULL, 
SENDER_NUMBER varchar(15) DEFAULT NULL, 
RECEIVER_NUMBER varchar(15) DEFAULT NULL, 
PRICE varchar(45) DEFAULT NULL, 
ERROR_MESSAGE varchar(255) DEFAULT NULL, 
ACCOUNT_SID varchar(255) DEFAULT NULL, 
NUM_MEDIA int DEFAULT NULL, 
ERROR_CODE varchar(20) DEFAULT NULL, 
SID varchar(255) DEFAULT NULL, 
MESSAGE_TYPE varchar(20) DEFAULT NULL, 
CREATED_ON timestamp NULL DEFAULT NULL, 
CREATED_BY varchar(100) DEFAULT NULL, 
SERVICE_TYPE varchar(100) DEFAULT NULL, 
PRIMARY KEY (SMS_ID)
);